package com.netease.nim.demo.lesRf.rfactivity.rfhome.bean;

public class Alarm {


    private int type;
    private String time;
    private String title;


    public int getType() {
        return type;
    }
    public void setType(int type) {
        this.type = type;
    }
    public String getTime() {
        return time;
    }
    public void setTime(String time) {
        this.time = time;
    }
    public String getTitle() {
        return title;
    }
    public void setTitle(String title) {
        this.title = title;
    }
}
